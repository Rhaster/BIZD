        WHEN OTHERS THEN
            loguj_informacje('ERROR', 'B≈ÇƒÖd podczas sprawdzania istnienia tabeli usuniete_dane_archiwum: ' || SQLERRM);
            RETURN;
    END;

    -- Sprawdzenie, czy kolumny istniejƒÖ w `archiwum_kursow_walut`
    BEGIN
        SELECT COUNT(*) INTO v_exists 
        FROM all_tab_columns 
        WHERE table_name = 'ARCHIWUM_KURSOW_WALUT' 
        AND column_name IN ('KOD_WALUTY', 'KURS', 'DATA_ARCHIWIZACJI');

        IF v_exists < 3 THEN
            loguj_informacje('ERROR', 'Brak wymaganych kolumn w tabeli archiwum_kursow_walut!');
            RETURN;
        END IF;
    EXCEPTION
        WHEN OTHERS THEN
            loguj_informacje('ERROR', 'B≈ÇƒÖd podczas sprawdzania kolumn w archiwum_kursow_walut: ' || SQLERRM);
            RETURN;
    END;

    -- Przeniesienie usuwanego rekordu do tabeli usuniete_dane_archiwum
    BEGIN
        INSERT INTO usuniete_dane_archiwum (kod_waluty, kurs, data_archiwizacji, data_usuniecia)
        VALUES (:OLD.kod_waluty, :OLD.kurs, :OLD.data_archiwizacji, SYSDATE);

        -- Logowanie operacji usuniƒôcia
        loguj_informacje('INFO', 'Usuniƒôto archiwalny kurs waluty ' || :OLD.kod_waluty || ', kurs: ' || :OLD.kurs);

        COMMIT;  -- Potrzebne dla autonomicznej transakcji
    EXCEPTION
        WHEN OTHERS THEN
            loguj_informacje('ERROR', 'B≈ÇƒÖd w triggerze trg_archiwizacja_usunietych_archiwum: ' || SQLERRM);
            ROLLBACK;  -- Wycofanie transakcji, je≈õli co≈õ posz≈Ço nie tak
    END;
END;
