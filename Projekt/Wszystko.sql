DECLARE
    PRAGMA AUTONOMOUS_TRANSACTION;  -- Umo≈ºliwia niezale≈ºne zapisanie log√≥w
    v_exists NUMBER;  -- Sprawdzenie, czy tabela `usuniete_dane_archiwum` istnieje
