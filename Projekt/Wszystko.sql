--Funkcja do pobrania wsystkich kurs√≥w danej waluty z aktualnych danych i archiwum 
CREATE OR REPLACE FUNCTION pobierz_kursy_waluty(p_kod_waluty IN VARCHAR2)
RETURN SYS_REFCURSOR
IS
    v_cursor SYS_REFCURSOR;
    v_count NUMBER;
BEGIN
    IF p_kod_waluty IS NULL THEN
        loguj_informacje('ERROR', 'Kod waluty nie mo≈ºe byƒá NULL');
        RAISE_APPLICATION_ERROR(-20001, 'Kod waluty nie mo≈ºe byƒá NULL');
    END IF;

    -- Sprawdzenie, czy w og√≥le istniejƒÖ dane dla podanej waluty
    SELECT COUNT(*) INTO v_count
    FROM (
        SELECT kod_waluty FROM kursy_walut1 WHERE kod_waluty = p_kod_waluty
        UNION ALL
        SELECT kod_waluty FROM archiwum_kursow_walut WHERE kod_waluty = p_kod_waluty
    );

    IF v_count = 0 THEN
        loguj_informacje('WARNING', 'Brak danych dla kodu waluty: ' || p_kod_waluty);
        RAISE_APPLICATION_ERROR(-20002, 'Brak danych dla podanego kodu waluty');
    END IF;

    loguj_informacje('INFO', 'Pobieranie kurs√≥w dla waluty: ' || p_kod_waluty);

    OPEN v_cursor FOR
        SELECT kod_waluty, kurs, data_aktualizacji AS data_operacji, 'Aktualny' AS zrodlo
        FROM kursy_walut1
        WHERE kod_waluty = p_kod_waluty
        
        UNION ALL
        
        SELECT kod_waluty, kurs, data_archiwizacji AS data_operacji, 'Archiwum' AS zrodlo
        FROM archiwum_kursow_walut
        WHERE kod_waluty = p_kod_waluty;

    RETURN v_cursor;
EXCEPTION
    WHEN OTHERS THEN
        loguj_informacje('ERROR', 'B≈ÇƒÖd w funkcji pobierz_kursy_waluty: ' || SQLERRM);
        RAISE_APPLICATION_ERROR(-20003, 'WystƒÖpi≈Ç nieoczekiwany b≈ÇƒÖd: ' || SQLERRM);
END pobierz_kursy_waluty;
/



DECLARE
    v_cursor SYS_REFCURSOR;
    v_kod_waluty VARCHAR2(3) := 'USD'; -- Przyk≈Çadowy kod waluty
    v_kurs NUMBER;
    v_data_operacji DATE;
    v_zrodlo VARCHAR2(20);
BEGIN
    v_cursor := pobierz_kursy_waluty(v_kod_waluty);

    LOOP
        FETCH v_cursor INTO v_kod_waluty, v_kurs, v_data_operacji, v_zrodlo;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE('Kod: ' || v_kod_waluty || ', Kurs: ' || v_kurs || ', Data: ' || v_data_operacji || ', ≈πr√≥d≈Ço: ' || v_zrodlo);
    END LOOP;

    CLOSE v_cursor;
END;
/

