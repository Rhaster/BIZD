-- Dodanie i aktualizacja kursu walut 


create or replace PROCEDURE DOD_LUB_AKTUALIZACJA_KW(
    p_kod_waluty VARCHAR2,
    p_kurs NUMBER
) AS
    v_exists NUMBER := 0;
BEGIN
    -- Sprawdzenie poprawno≈õci kodu waluty
    IF NOT REGEXP_LIKE(p_kod_waluty, '^[A-Z]{3}$') THEN
        loguj_informacje('ERROR', 'Nieprawid≈Çowy kod waluty: ' || p_kod_waluty);
        RAISE_APPLICATION_ERROR(-20002, 'Kod waluty musi sk≈Çadaƒá siƒô z 3 du≈ºych liter.');
    END IF;

    -- Sprawdzenie poprawno≈õci kursu
    IF NOT sprawdz_poprawnosc_kursu(p_kurs) THEN
        loguj_informacje('ERROR', 'Nieprawid≈Çowa warto≈õƒá kursu dla ' || p_kod_waluty);
        RAISE_APPLICATION_ERROR(-20001, 'Kurs waluty musi byƒá dodatni.');
    END IF;

    -- Sprawdzenie, czy waluta istnieje
    BEGIN
        SELECT 1 INTO v_exists FROM KURSY_WALUT1 WHERE kod_waluty = p_kod_waluty FETCH FIRST ROW ONLY;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            v_exists := 0;
    END;

    -- Je≈õli istnieje, aktualizujemy kurs
    IF v_exists > 0 THEN
        BEGIN
            UPDATE KURSY_WALUT1
            SET kurs = p_kurs, data_aktualizacji = SYSDATE
            WHERE kod_waluty = p_kod_waluty;
            loguj_informacje('INFO', 'Zaktualizowano kurs waluty ' || p_kod_waluty);
        EXCEPTION
            WHEN OTHERS THEN
                loguj_informacje('ERROR', 'Nie uda≈Ço siƒô zaktualizowaƒá kursu: ' || SQLERRM);
        END;
    ELSE
        -- Je≈õli nie istnieje, dodajemy nowy wpis
        BEGIN
            INSERT INTO KURSY_WALUT1 (kod_waluty, kurs, data_aktualizacji)
            VALUES (p_kod_waluty, p_kurs, SYSDATE);
            loguj_informacje('INFO', 'Dodano nowy kurs waluty ' || p_kod_waluty);
        EXCEPTION
            WHEN DUP_VAL_ON_INDEX THEN
                loguj_informacje('ERROR', 'Pr√≥ba dodania duplikatu: ' || SQLERRM);
            WHEN OTHERS THEN
                loguj_informacje('ERROR', 'Nie uda≈Ço siƒô dodaƒá kursu: ' || SQLERRM);
        END;
    END IF;

    -- Zako≈Ñczenie transakcji (bezpiecze≈Ñstwo)
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        loguj_informacje('ERROR', 'B≈ÇƒÖd w procedurze: ' || SQLERRM);
        RAISE;
END;


