-- Funkcja pobierz sredni kurs walut( od podanego do okreslonego okresu, jedna waluta) 
CREATE OR REPLACE FUNCTION pobierz_sredni_kurs_waluty(
    p_kod_waluty VARCHAR2, -- Kod waluty, dla kt√≥rej obliczamy ≈õredni kurs
    p_data_od VARCHAR2, -- PoczƒÖtkowa data zakresu
    p_data_do VARCHAR2 -- Ko≈Ñcowa data zakresu
) RETURN NUMBER
IS
    v_sredni_kurs NUMBER := NULL; -- Zmienna przechowujƒÖca wynikowy ≈õredni kurs
    v_exists NUMBER := 0; -- Flaga sprawdzajƒÖca, czy waluta istnieje w bazie
    v_data_od DATE; -- Zmienna przechowujƒÖca sformatowanƒÖ datƒô poczƒÖtkowƒÖ
    v_data_do DATE; -- Zmienna przechowujƒÖca sformatowanƒÖ datƒô ko≈ÑcowƒÖ
BEGIN
    -- Konwersja parametr√≥w na format daty zgodny z tabelami
    BEGIN
        v_data_od := TO_DATE(p_data_od, 'DD/MM/YY'); -- Konwersja daty poczƒÖtkowej
        v_data_do := TO_DATE(p_data_do, 'DD/MM/YY'); -- Konwersja daty ko≈Ñcowej
    EXCEPTION
        WHEN OTHERS THEN
            loguj_informacje('ERROR', 'Nieprawid≈Çowy format daty: ' || p_data_od || ' - ' || p_data_do); -- Logowanie b≈Çƒôdu formatu daty
            RETURN NULL; -- Zwracamy NULL w przypadku b≈Çƒôdu
    END;

    -- Sprawdzenie, czy waluta istnieje w kursy_walut1 lub archiwum_kursow_walut
    BEGIN
        SELECT COUNT(*) INTO v_exists 
        FROM (
            SELECT 1 FROM kursy_walut1 WHERE kod_waluty = p_kod_waluty
            UNION ALL
            SELECT 1 FROM archiwum_kursow_walut WHERE kod_waluty = p_kod_waluty
        );

        IF v_exists = 0 THEN
            loguj_informacje('ERROR', 'Brak kursu waluty ' || p_kod_waluty || ' w bazie.'); -- Logowanie braku waluty w bazie
            RETURN NULL; -- Zwracamy NULL, je≈õli waluty nie ma
        END IF;
    EXCEPTION
        WHEN OTHERS THEN
            RETURN NULL; -- W przypadku b≈Çƒôdu zwracamy NULL
    END;

    -- Pobranie ≈õredniego kursu waluty (uwzglƒôdniajƒÖc TRUNC)
    BEGIN
        SELECT AVG(kurs) INTO v_sredni_kurs
        FROM (
            SELECT kurs FROM kursy_walut1 
            WHERE kod_waluty = p_kod_waluty
            AND TRUNC(data_aktualizacji) BETWEEN v_data_od AND v_data_do
            UNION ALL
            SELECT kurs FROM archiwum_kursow_walut 
            WHERE kod_waluty = p_kod_waluty
            AND TRUNC(data_archiwizacji) BETWEEN v_data_od AND v_data_do
        );

        -- Je≈õli wynik jest NULL, logujemy
        IF v_sredni_kurs IS NULL THEN
            loguj_informacje('ERROR', 'Brak danych dla waluty ' || p_kod_waluty || ' w podanym zakresie.'); -- Logowanie braku danych
            RETURN NULL; -- Zwracamy NULL, je≈õli nie znaleziono danych
        END IF;

        -- Logowanie sukcesu
        loguj_informacje('INFO', 'Pobrano ≈õredni kurs waluty ' || p_kod_waluty || 
            ' za okres ' || p_data_od || ' - ' || p_data_do);

    EXCEPTION
        WHEN OTHERS THEN
            loguj_informacje('ERROR', 'B≈ÇƒÖd w funkcji pobierz_sredni_kurs_waluty: ' || SQLERRM); -- Logowanie b≈Çƒôdu funkcji
            RETURN NULL; -- Zwracamy NULL w przypadku b≈Çƒôdu
    END;

    RETURN v_sredni_kurs; -- Zwracamy obliczony ≈õredni kurs waluty
END pobierz_sredni_kurs_waluty;
/

