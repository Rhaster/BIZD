-- Procedura stw√≥rz zestawiania dla wszystkich walut od danego okresu, do danego okresu 


CREATE OR REPLACE PROCEDURE generuj_zestawienia_dla_wszystkich_walut(
    p_nazwa_zestawienia VARCHAR2,  -- Nazwa zestawienia np. "Zestawienie Stycze≈Ñ 2025"
    p_data_od DATE,
    p_data_do DATE
) AS
    v_id_lista_zestawien NUMBER;
    v_kod_waluty VARCHAR2(3);
    v_count NUMBER;

    -- Kursor pobierajƒÖcy unikalne waluty z obu tabel
    CURSOR c_waluty IS (
        SELECT DISTINCT kod_waluty FROM kursy_walut1
        UNION
        SELECT DISTINCT kod_waluty FROM archiwum_kursow_walut
    );
BEGIN
    -- üîπ 1Ô∏è‚É£ Sprawdzenie poprawno≈õci dat
    IF p_data_od > p_data_do THEN
        loguj_informacje('ERROR', 'B≈ÇƒÖd: Data OD (' || TO_CHAR(p_data_od, 'YYYY-MM-DD') || ') jest p√≥≈∫niejsza ni≈º data DO (' || TO_CHAR(p_data_do, 'YYYY-MM-DD') || ')');
        RAISE_APPLICATION_ERROR(-20001, 'Data OD nie mo≈ºe byƒá p√≥≈∫niejsza ni≈º data DO');
    END IF;

    -- üîπ 2Ô∏è‚É£ Sprawdzenie, czy istniejƒÖ kursy walutowe w danym zakresie
    SELECT COUNT(*) INTO v_count 
    FROM (
        SELECT kod_waluty FROM kursy_walut1 WHERE data_aktualizacji BETWEEN p_data_od AND p_data_do
        UNION
        SELECT kod_waluty FROM archiwum_kursow_walut WHERE data_archiwizacji BETWEEN p_data_od AND p_data_do
    );
    
    IF v_count = 0 THEN
        loguj_informacje('ERROR', 'Brak danych o kursach w podanym okresie ' || TO_CHAR(p_data_od, 'YYYY-MM-DD') || ' - ' || TO_CHAR(p_data_do, 'YYYY-MM-DD'));
        RAISE_APPLICATION_ERROR(-20002, 'Brak danych o kursach w podanym okresie.');
    END IF;

    -- üîπ 3Ô∏è‚É£ Tworzenie nowego wpisu w `lista_zestawien`
    BEGIN
        INSERT INTO lista_zestawien (nazwa) 
        VALUES (p_nazwa_zestawienia)
        RETURNING id_zestawienia INTO v_id_lista_zestawien;

        COMMIT;

        -- Logowanie operacji
        loguj_informacje('INFO', 'Utworzono nowe zestawienie: ' || p_nazwa_zestawienia || ', ID: ' || v_id_lista_zestawien);
    EXCEPTION
        WHEN OTHERS THEN
            loguj_informacje('ERROR', 'B≈ÇƒÖd podczas tworzenia wpisu w lista_zestawien: ' || SQLERRM);
            ROLLBACK;
            RAISE;
    END;

    -- üîπ 4Ô∏è‚É£ Pobranie i przetworzenie wszystkich walut
    OPEN c_waluty;
    LOOP
        FETCH c_waluty INTO v_kod_waluty;
        EXIT WHEN c_waluty%NOTFOUND;

        BEGIN
            -- üîπ Wywo≈Çanie procedury dla ka≈ºdej waluty
            dodaj_zestawienie_walutowe(v_id_lista_zestawien, v_kod_waluty, p_data_od, p_data_do);
            
            -- üîπ Logowanie sukcesu dla danej waluty
            loguj_informacje('INFO', 'Zestawienie dla waluty ' || v_kod_waluty || ' wygenerowane pomy≈õlnie.');
        EXCEPTION
            WHEN OTHERS THEN
                -- üîπ Logowanie b≈Çƒôdu dla danej waluty
                loguj_informacje('ERROR', 'B≈ÇƒÖd podczas generowania zestawienia dla waluty ' || v_kod_waluty || ': ' || SQLERRM);
        END;
    END LOOP;
    CLOSE c_waluty;

    -- üîπ 5Ô∏è‚É£ Potwierdzenie zako≈Ñczenia procedury
    loguj_informacje('INFO', 'Zako≈Ñczono generowanie zestawie≈Ñ dla wszystkich walut.');

EXCEPTION
    WHEN OTHERS THEN
        loguj_informacje('ERROR', 'B≈ÇƒÖd w procedurze generuj_zestawienia_dla_wszystkich_walut: ' || SQLERRM);
        ROLLBACK;
        RAISE;
END;
/


